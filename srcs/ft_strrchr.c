char	*ft_strrchr(const char *s, int c)
{
	char	*str;
	char	q;
	int		i;

	str = (char *)s;
	q = c;
	i = 0;
	while (*str++)
		i++;
	while ((i--) + 1)
		if (*(--str) == q)
			return (str);
	return ((void *)0);
}
